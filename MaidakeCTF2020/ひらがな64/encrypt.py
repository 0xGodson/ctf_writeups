def str_to_bin(s):
    bin_str = ""
    for c in s:
        for i in range(BYTE_SIZE):
            bin_str += str((ord(c) >> (BYTE_SIZE - (i + 1))) & 1)
    return bin_str

def fill(s, n, c):
    mod = len(s) % n
    if mod == 0: return s 
    margin = n - mod
    return s + c * margin

